    private void cargarServiciosEnComboBox() {
        try {
            Connection connection = MySQL.getConnection();
            List<Servicios> servicios = Queries.getServicios(connection);

            String[] nombresServicios = servicios.stream().map(Servicios::getNombre).toArray(String[]::new);
            DefaultComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>(nombresServicios);

            jComboBox1.setModel(comboBoxModel);

            MySQL.closeConnection(connection);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void cargarRepuestosEnComboBox() {
        try {
            Connection connection = MySQL.getConnection();
            List<Repuestos> repuestos = Queries.getRepuestos(connection);

            String[] nombresRepuestos = repuestos.stream().map(Repuestos::getNombre).toArray(String[]::new);
            DefaultComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>(nombresRepuestos);

            jComboBox2.setModel(comboBoxModel);

            MySQL.closeConnection(connection);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

