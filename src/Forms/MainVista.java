    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedIndexServicio = jComboBox1.getSelectedIndex();
        int selectedIndexRepuesto = jComboBox2.getSelectedIndex();

        if (selectedIndexServicio != -1 && selectedIndexRepuesto != -1) {
            try {
                Connection connection = MySQL.getConnection();
                List<Servicios> servicios = Queries.getServicios(connection);
                List<Repuestos> repuestos = Queries.getRepuestos(connection);

                Servicios servicioSeleccionado = servicios.get(selectedIndexServicio);
                Repuestos repuestoSeleccionado = repuestos.get(selectedIndexRepuesto);

                double precioTotal = servicioSeleccionado.getPrecio() + repuestoSeleccionado.getPrecio();
                JOptionPane.showMessageDialog(this, "Precio Total: $" + precioTotal, "Precio Total", JOptionPane.INFORMATION_MESSAGE);

                // Obtener la placa directamente del jLabel2
                String placaVehiculo = jLabel2.getText().replace("Placa: ", "").trim();

                Log log = new Log(
                        placaVehiculo,
                        servicioSeleccionado.getNombre(),
                        repuestoSeleccionado.getNombre(),
                        new Timestamp(System.currentTimeMillis())
                );

                Queries.insertLog(connection, log);
                JOptionPane.showMessageDialog(this, "Servicio registrado", "Correctamente", JOptionPane.INFORMATION_MESSAGE);
                MySQL.closeConnection(connection);
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

